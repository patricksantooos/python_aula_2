def testeDoNumero():
  numero = int(input("Digite um numero inteiro positivo: "))
  if numero == 1:
    print("Um!")
  elif numero == 2:
    print("Dois!")
  elif numero == 3:
    print("Três!")
  elif numero == 4:
    print("Quatro!")
  elif numero == 5:
    print("Cinco!")
  else:
    print("Número inválido!")

testeDoNumero()
